//task8: print numbers from 1 to 10 but skip number 5 using 'continue'.
for (let i = 1; i <= 10; i++) {
  if (i === 5) {
    continue;
  }
  console.log(i);
}

//task9: print numbers from 1 to 10 but stop the loop at number 7 using 'break'.
for (let i = 1; i <= 10; i++) {
  if (i === 7) {
    break;
  }
  console.log(i);
}
